-- Purpose: audit and clean malformed UUID-like values in lectures table for Azure/Postgres
-- Run in a transaction and validate output before COMMIT in production.

BEGIN;

-- 1) Audit malformed rows (id / teacher_id / batch_id not UUID text)
SELECT
  id,
  teacher_id,
  batch_id,
  institute_id,
  title,
  scheduled_at,
  created_at
FROM lectures
WHERE id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR teacher_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR batch_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$';

-- 2) Backup malformed rows before delete (create backup table once)
CREATE TABLE IF NOT EXISTS lectures_bad_uuid_backup AS
SELECT * FROM lectures WHERE FALSE;

INSERT INTO lectures_bad_uuid_backup
SELECT *
FROM lectures
WHERE id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR teacher_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR batch_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$';

-- 3) Delete malformed rows (recommended when values are unrecoverable text)
DELETE FROM lectures
WHERE id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR teacher_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR batch_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$';

-- 4) Verify no malformed rows remain
SELECT COUNT(*) AS malformed_remaining
FROM lectures
WHERE id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR teacher_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
   OR batch_id::text !~* '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$';

-- If results look correct, COMMIT; otherwise ROLLBACK.
-- COMMIT;
ROLLBACK;
